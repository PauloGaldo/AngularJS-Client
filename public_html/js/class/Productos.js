function Productos(productoId, marcas, tiposProductos, zonaActuacion, nombre, fechaVencimiento, precioUnitario, stock, imagen, estado, fechaCreacion, fechaModificacion, usuarioCreacion, usuarioModificacion) {
    this.productoId = productoId;
    this.marcas = marcas;
    this.tiposProductos = tiposProductos;
    this.zonaActuacion = zonaActuacion;
    this.nombre = nombre;
    this.fechaVencimiento = fechaVencimiento;
    this.precioUnitario = precioUnitario;
    this.stock = stock;
    this.imagen = imagen;
    this.estado = estado;
    this.fechaCreacion = fechaCreacion;
    this.fechaModificacion = fechaModificacion;
    this.usuarioCreacion = usuarioCreacion;
    this.usuarioModificacion = usuarioModificacion;
}

